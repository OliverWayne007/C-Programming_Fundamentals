#include <stdio.h>

int main()
{
    int a , b;
    
    printf("Enter the value of a: ");
    scanf("%d" , &a);
    printf("Enter the value of b: ");
    scanf("%d" , &b);

    // Addition
    printf("The sum of a and b is: %d\n" , a + b);
    // Substraction
    printf("The sum of a and b is: %d\n" , a - b);
    // Multiplication
    printf("The sum of a and b is: %d\n" , a * b);
    // Division
    printf("The sum of a and b is: %d\n" , a / b);

    return 0;
}